package handlers

import (
	"mcpdocs/schema"
	"mcpdocs/services"
	"net/http"
	"strconv"

	"github.com/gin-gonic/gin"
)

type IndexingHandler struct {
	indexingService services.IndexingServiceInterface
}

func NewIndexingHandler(indexingService services.IndexingServiceInterface) *IndexingHandler {
	return &IndexingHandler{indexingService: indexingService}
}

func (h *IndexingHandler) CreateIndexingRequest(c *gin.Context) {
	userID := c.GetString("userID")
	if userID == "" {
		c.JSON(http.StatusUnauthorized, gin.H{"error": "Unauthorized"})
		return
	}

	var req schema.CreateIndexingRequestRequest
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}

	request, err := h.indexingService.CreateIndexingRequest(c.Request.Context(), userID, req)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusCreated, request)
}

func (h *IndexingHandler) GetIndexingRequest(c *gin.Context) {
	idStr := c.Param("id")
	id, err := strconv.ParseUint(idStr, 10, 32)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "Invalid ID"})
		return
	}

	request, err := h.indexingService.GetIndexingRequest(c.Request.Context(), uint(id))
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}

	c.JSON(http.StatusOK, request)
}

